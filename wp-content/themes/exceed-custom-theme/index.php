          }
        });
      },
      {
        root: null, // ビューポートを基準
        threshold: 0.1, // 10%が表示されたら発火
      }
    );

    observer.observe(target); // `.company-cnt`を監視
  } else {
    console.error("`.company-cnt` または `.company-title` 要素が見つかりませんでした。");
  }
});
